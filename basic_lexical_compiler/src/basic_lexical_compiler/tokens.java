package basic_lexical_compiler;

public class tokens {

	public int token_id;
	public String token_group;
	public String token;
	
	public int getToken_id() {
		return token_id;
	}
	public void setToken_id(int token_id) {
		this.token_id = token_id;
	}
	public String getToken_group() {
		return token_group;
	}
	public void setToken_group(String token_group) {
		this.token_group = token_group;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
}
