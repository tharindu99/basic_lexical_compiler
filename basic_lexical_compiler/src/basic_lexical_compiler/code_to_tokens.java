package basic_lexical_compiler;

public class code_to_tokens {
	int pos_id;
	String token_cat;
	String word;
	
	public int getPos_id() {
		return pos_id;
	}
	public void setPos_id(int pos_id) {
		this.pos_id = pos_id;
	}
	public String getToken_cat() {
		return token_cat;
	}
	public void setToken_cat(String token_cat) {
		this.token_cat = token_cat;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	

}
